    private void updateTable() {

        String updateQuery = "select * from donanım";

        try {

            prepared = connect.prepareStatement(updateQuery);
            resultSet = prepared.executeQuery();
            table.setModel(DbUtils.resultSetToTableModel(resultSet));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }

    }

    private void updateCombobox() {

        String sql = "select * from donanım";
        try {

            prepared = connect.prepareStatement(sql);
            resultSet = prepared.executeQuery();

            while (resultSet.next()) {
                jComboBox1.addItem(resultSet.getString("bildirim_sicili"));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }

    }

