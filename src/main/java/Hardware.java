    private void search_hardwareActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_hardwareActionPerformed

        try {

            String sql = "select * from donanım where seri_no = ?";

            prepared = connect.prepareStatement(sql);
            prepared.setString(1, jTextField1.getText());

            resultSet = prepared.executeQuery();
            table.setModel(DbUtils.resultSetToTableModel(resultSet));
            if (resultSet.next()) {

                jComboBox1.setSelectedItem(resultSet.getString("bildirim_sicili"));
                merkez_combobox.setSelectedItem(resultSet.getString("merkez_adı"));
                şube_combobox.setSelectedItem(resultSet.getString("Şube_adı"));
                seri_no_giris.setText(resultSet.getString("seri_no"));
                cihaz_modeli_giris.setText(resultSet.getString("cihaz_modeli"));
                bildirim_sicili_giris.setText(resultSet.getString("bildirim_sicili"));
                ariza_acıklamasi_giris.setText(resultSet.getString("arıza_açıklaması"));
                ariza_tarihi_giris.setText(resultSet.getString("arıza_tarihi"));

            }
            if (jTextField1.getText().isEmpty()) {
                updateTable();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        updateCombobox();


    }//GEN-LAST:event_search_hardwareActionPerformed

